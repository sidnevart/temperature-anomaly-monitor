import numpy as np
import pandas as pd
import plotly.graph_objects as go
import streamlit as st

st.set_page_config(page_title="Обучение — Anomaly Monitor", page_icon="📚", layout="wide")

st.title("📚 Как работает обнаружение аномалий")
st.markdown("Эта страница объясняет ключевые концепции проекта простым языком — без формул, с картинками и интерактивными примерами.")

# ─────────────────────────────────────────────
# 1. ЧТО ТАКОЕ АНОМАЛИЯ
# ─────────────────────────────────────────────
st.header("1. Что такое аномалия?")
st.markdown("""
Представьте термометр в комнате. Обычно он показывает **20–22 °C**.
Если вдруг показал **35 °C** — это **аномалия**: что-то не так.

**Аномалия** — это отклонение от нормального поведения.
В нашем проекте «норма» — это плавные колебания температуры датчика вокруг базовой точки (~70 °C).
""")

col1, col2 = st.columns(2)
with col1:
    st.info("**Норма** 🟢\n\nТемпература плавно колеблется: 68 → 70 → 71 → 69 → 70 °C")
with col2:
    st.error("**Аномалия** 🔴\n\nРезкий скачок: 70 → 70 → **85** → 70 °C\nили датчик завис на одном значении")

# ─────────────────────────────────────────────
# 2. СКОЛЬЗЯЩЕЕ СРЕДНЕЕ
# ─────────────────────────────────────────────
st.header("2. Скользящее среднее")
st.markdown("""
Чтобы понять, «нормально» ли сейчас, нужно знать, что было **недавно**.
Скользящее среднее — это **среднее значение за последние N точек**.

Например, с окном = 5: для каждой точки берём её и 4 предыдущие, считаем среднее.
Это **сглаживает** шум и показывает общий тренд.
""")

window = st.slider("Размер окна скользящего среднего", 2, 30, 10, key="ma_window")

# Генерируем простой сигнал
np.random.seed(42)
t = np.arange(100)
base = 70 + 3 * np.sin(2 * np.pi * t / 50)
noise = np.random.normal(0, 1, 100)
signal = base + noise
# Добавим аномалию
signal[60:65] += 15  # скачок

df_ma = pd.DataFrame({"t": t, "Температура": signal})
df_ma["Скользящее среднее"] = df_ma["Температура"].rolling(window=window, min_periods=1).mean()

fig_ma = go.Figure()
fig_ma.add_trace(go.Scatter(x=t, y=signal, mode="lines", name="Температура",
                            line=dict(color="#4ea8de", width=1), opacity=0.6))
fig_ma.add_trace(go.Scatter(x=t, y=df_ma["Скользящее среднее"], mode="lines",
                            name=f"Среднее (окно={window})",
                            line=dict(color="#00d4aa", width=2.5)))
fig_ma.add_vrect(x0=60, x1=65, fillcolor="rgba(255,71,87,0.15)", line_width=0,
                 annotation_text="Аномалия", annotation_position="top left")
fig_ma.update_layout(height=350, margin=dict(l=0, r=0, t=30, b=0),
                     legend=dict(orientation="h", yanchor="bottom", y=1.02, xanchor="right", x=1))
st.plotly_chart(fig_ma, use_container_width=True)

st.markdown("""
👆 Попробуйте двигать ползунок. При **маленьком** окне среднее быстро реагирует на скачок.
При **большом** — сглаживает его, и скачок менее заметен.

В проекте используется окно **10 точек** — компромисс между скоростью реакции и устойчивостью к шуму.
""")

# ─────────────────────────────────────────────
# 3. Z-SCORE
# ─────────────────────────────────────────────
st.header("3. Z-score: насколько точка «странная»?")
st.markdown("""
Z-score отвечает на вопрос: **«Насколько эта точка далеко от среднего?»**

**Формула:** `z = (температура − среднее) / стандартное отклонение`

- z = 0 → точно на среднем
- z = 1 → на 1 стандартное отклонение от среднего (нормально)
- z = 3 → уже далеко (подозрительно)
- z > 3 → очень далеко (аномалия)
""")

z_threshold = st.slider("Порог z-score для аномалии", 1.0, 5.0, 3.0, 0.1, key="z_thresh")

# Вычисляем z-score
rolling_mean = pd.Series(signal).rolling(window=10, min_periods=1).mean()
rolling_std = pd.Series(signal).rolling(window=10, min_periods=1).std().replace(0, 1e-6)
z_scores = ((signal - rolling_mean) / rolling_std).abs()

fig_z = go.Figure()
fig_z.add_trace(go.Scatter(x=t, y=z_scores, mode="lines+markers", name="|z-score|",
                           line=dict(color="#a78bfa", width=1.5),
                           marker=dict(size=4)))
fig_z.add_hline(y=z_threshold, line_dash="dash", line_color="#ff4757",
                annotation_text=f"Порог = {z_threshold}")
fig_z.update_layout(height=300, margin=dict(l=0, r=0, t=30, b=0),
                    yaxis_title="|z-score|", xaxis_title="Время",
                    legend=dict(orientation="h", yanchor="bottom", y=1.02, xanchor="right", x=1))
st.plotly_chart(fig_z, use_container_width=True)

anomaly_count = int((z_scores > z_threshold).sum())
st.markdown(f"👆 При пороге **{z_threshold}** найдено **{anomaly_count}** точек выше порога. "
            f"В проекте используется порог **3.0** — стандартное значение для статистических аномалий.")

# ─────────────────────────────────────────────
# 4. СКОРОСТЬ ИЗМЕНЕНИЯ
# ─────────────────────────────────────────────
st.header("4. Скорость изменения (temp_diff)")
st.markdown("""
Иногда важна не сама температура, а **как быстро она меняется**.

Если между двумя соседними точками разница **> 5 °C** — это резкий скачок.
Такое в реальной системе означает: датчик сломался, произошёл выброс, или авария.
""")

diff_values = np.abs(np.diff(signal, prepend=signal[0]))

fig_diff = go.Figure()
fig_diff.add_trace(go.Bar(x=t, y=diff_values, name="|ΔT|",
                          marker_color=np.where(diff_values > 5, "#ff4757", "#4ea8de")))
fig_diff.add_hline(y=5, line_dash="dash", line_color="#ff4757",
                   annotation_text="Порог = 5 °C")
fig_diff.update_layout(height=250, margin=dict(l=0, r=0, t=30, b=0),
                       yaxis_title="|ΔT|, °C", xaxis_title="Время",
                       legend=dict(orientation="h", yanchor="bottom", y=1.02, xanchor="right", x=1))
st.plotly_chart(fig_diff, use_container_width=True)

# ─────────────────────────────────────────────
# 5. ПРАВИЛА vs ML
# ─────────────────────────────────────────────
st.header("5. Правила vs ML-модель")
st.markdown("""
В проекте используется **два подхода** одновременно:
""")

c1, c2 = st.columns(2)
with c1:
    st.subheader("📏 Правила")
    st.markdown("""
    **Что это:** фиксированные условия, которые понятны человеку.

    - «Если скачок > 5 °C → аномалия»
    - «Если датчик не менялся 10 точек → завис»
    - «Если z-score > 3 → отклонение»

    **Плюсы:**
    - ✅ Объяснимы оператору («почему сработала тревога?»)
    - ✅ Быстрые, не нужна модель
    - ✅ Работают «из коробки»

    **Минусы:**
    - ❌ Нужно вручную настраивать пороги
    - ❌ Не ловят сложные паттерны (например, медленный дрейф)
    """)

with c2:
    st.subheader("🤖 Isolation Forest (ML)")
    st.markdown("""
    **Что это:** алгоритм, который сам находит «странные» точки в данных.

    Он смотрит на 10 признаков одновременно (температуру, скорость, z-score…)
    и решает: типичная это точка или нет.

    **Плюсы:**
    - ✅ Ловит сложные паттерны, которые правилами не опишешь
    - ✅ Не нужно знать заранее, какие аномалии бывают
    - ✅ Адаптируется к данным

    **Минусы:**
    - ❌ Непонятно, почему сработала («чёрный ящик»)
    - ❌ Нужно обучать на данных
    """)

st.markdown("""
---
**В проекте:** правила и ML работают **параллельно**. Если хотя бы один нашёл аномалию —
точка считается аномальной. Это **гибридный подход**: объяснимость правил + гибкость ML.
""")

# ─────────────────────────────────────────────
# 6. КАК ОБУЧАЕТСЯ ISOLATION FOREST
# ─────────────────────────────────────────────
st.header("6. Как обучается Isolation Forest?")
st.markdown("""
Обучение в нашем проекте отличается от того, что обычно делают:

**❌ Плохой способ:** обучить на всех данных (включая аномалии).
Модель «запомнит» аномалии и не научится их находить.

**✅ Наш способ:** обучить **только на нормальных данных** (сценарий `normal`).
Тогда модель знает, как выглядит норма. Всё, что от неё отличается — аномалия.

Это называется **обучение без утечки данных** (no data leakage).
""")

st.markdown("""
#### Что видит модель?

Модель получает **10 числовых признаков** для каждой точки:

| Признак | Что означает |
|---------|-------------|
| `temperature_filled` | Температура (с заполненными пропусками) |
| `rolling_mean` | Скользящее среднее |
| `rolling_std` | Скользящее стандартное отклонение |
| `temp_diff` | Разница с предыдущей точкой |
| `abs_temp_diff` | Модуль разницы |
| `abs_z_score` | Модуль z-score |
| `is_missing` | Был ли пропуск (0/1) |
| `is_stuck` | Завис ли датчик (0/1) |
| `abs_diff_from_group_mean` | Отклонение от среднего по группе |
| `rolling_temp_diff_mean_20` | Средняя скорость изменения за 20 точек |
""")

st.markdown("""
#### Что выдаёт модель?

Для каждой точки — **anomaly score** от 0 до 1:

""")

col_a, col_b, col_c = st.columns(3)
col_a.metric("🟢 0.00 – 0.60", "Норма", "Штатный режим")
col_b.metric("🟡 0.60 – 0.85", "Warning", "Подозрительно")
col_c.metric("🔴 > 0.85", "High", "Высокий риск")

